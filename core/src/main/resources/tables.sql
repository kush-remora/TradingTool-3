-- Stock universe source of truth. Each row is one stock membership in an index
-- or maintained watchlist (for example index_key = 'groww').
CREATE TABLE IF NOT EXISTS public.index_constituents (
    index_key        TEXT        NOT NULL,
    symbol           TEXT        NOT NULL,
    instrument_token BIGINT      NOT NULL,
    company_name     TEXT        NOT NULL DEFAULT '',
    industry         TEXT        NOT NULL DEFAULT '',
    series           TEXT        NOT NULL DEFAULT '',
    isin_code        TEXT        NOT NULL DEFAULT '',
    is_active        BOOLEAN     NOT NULL DEFAULT true,
    source_url       TEXT        NOT NULL,
    last_synced_at   TIMESTAMPTZ NOT NULL,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (index_key, symbol)
);

CREATE INDEX IF NOT EXISTS idx_index_constituents_symbol
    ON public.index_constituents (symbol);
CREATE INDEX IF NOT EXISTS idx_index_constituents_instrument_token
    ON public.index_constituents (instrument_token);
CREATE INDEX IF NOT EXISTS idx_index_constituents_index_active
    ON public.index_constituents (index_key, is_active);

-- Kite tokens: persists Kite Connect access tokens across service restarts.
CREATE TABLE IF NOT EXISTS public.kite_tokens (
    id           SERIAL PRIMARY KEY,
    access_token TEXT        NOT NULL,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_kite_tokens_created ON public.kite_tokens(created_at DESC);

-- Trades: consolidated trade journal keyed directly by instrument token.
CREATE TABLE IF NOT EXISTS public.trades (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    instrument_token  BIGINT      NOT NULL,
    nse_symbol        TEXT        NOT NULL,
    quantity          INTEGER     NOT NULL,
    avg_buy_price     NUMERIC(10, 2) NOT NULL,
    today_low         NUMERIC(10, 2),
    stop_loss_percent NUMERIC(5, 2)  NOT NULL,
    stop_loss_price   NUMERIC(10, 2) NOT NULL,
    close_price       NUMERIC(12, 2) NULL,
    close_date        DATE          NULL,
    notes             TEXT,
    trade_date        DATE        NOT NULL,
    strategy          TEXT,
    created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(instrument_token)  -- one consolidated position per instrument
);

ALTER TABLE public.trades
    ADD COLUMN IF NOT EXISTS strategy TEXT;

CREATE INDEX IF NOT EXISTS idx_trades_instrument_token ON public.trades(instrument_token);
CREATE INDEX IF NOT EXISTS idx_trades_instrument_token_date ON public.trades(instrument_token, trade_date);
CREATE INDEX IF NOT EXISTS idx_trades_created_at ON public.trades(created_at DESC);





-- Daily candles: one row per (instrument, date) for long-term metrics
CREATE TABLE IF NOT EXISTS public.daily_candles (
    instrument_token  BIGINT         NOT NULL,
    symbol            TEXT           NOT NULL,
    candle_date       DATE           NOT NULL,
    open              NUMERIC(12, 4) NOT NULL,
    high              NUMERIC(12, 4) NOT NULL,
    low               NUMERIC(12, 4) NOT NULL,
    close             NUMERIC(12, 4) NOT NULL,
    volume            BIGINT         NOT NULL,
    created_at        TIMESTAMPTZ    NOT NULL DEFAULT NOW(),
    PRIMARY KEY (instrument_token, candle_date)
);

-- Intraday candles: one row per (instrument, interval, timestamp) for intraday patterns
CREATE TABLE IF NOT EXISTS public.intraday_candles (
    instrument_token  BIGINT         NOT NULL,
    symbol            TEXT           NOT NULL,
    interval          TEXT           NOT NULL,
    candle_timestamp  TIMESTAMP      NOT NULL, -- always IST, no timezone (Kite always returns IST)
    open              NUMERIC(12, 4) NOT NULL,
    high              NUMERIC(12, 4) NOT NULL,
    low               NUMERIC(12, 4) NOT NULL,
    close             NUMERIC(12, 4) NOT NULL,
    volume            BIGINT         NOT NULL,
    created_at        TIMESTAMPTZ    NOT NULL DEFAULT NOW(),
    PRIMARY KEY (instrument_token, interval, candle_timestamp)
);

-- Stock delivery data: daily delivery metrics for each stock
CREATE TABLE IF NOT EXISTS public.stock_delivery_daily (
    instrument_token BIGINT NOT NULL,
    symbol TEXT NOT NULL,
    exchange TEXT NOT NULL,
    universe TEXT NOT NULL CHECK (universe IN ('largemidcap250', 'smallcap250', 'watchlist')),
    trading_date DATE NOT NULL,
    reconciliation_status TEXT NOT NULL CHECK (reconciliation_status IN ('PRESENT', 'MISSING_FROM_SOURCE')),
    series TEXT,
    ttl_trd_qnty BIGINT,
    deliv_qty BIGINT,
    deliv_per DECIMAL(10, 2),
    source_file_name TEXT,
    source_url TEXT,
    fetched_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (instrument_token, trading_date)
);

-- Groww volume shockers: complete daily top-100 snapshot before strategy filtering.
CREATE TABLE IF NOT EXISTS public.groww_volume_shocker_daily (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trade_date              DATE          NOT NULL,
    source_rank             SMALLINT      NOT NULL,
    exchange                TEXT          NOT NULL,
    symbol                  TEXT          NOT NULL,
    instrument_token        BIGINT        NOT NULL,
    company_name            TEXT          NOT NULL,
    ltp                     NUMERIC(18, 4) NOT NULL,
    close                   NUMERIC(18, 4) NOT NULL,
    market_cap_crore        NUMERIC(20, 2) NOT NULL,
    market_cap_category     TEXT          NOT NULL,
    year_low                NUMERIC(18, 4) NOT NULL,
    year_high               NUMERIC(18, 4) NOT NULL,
    volume                  BIGINT        NOT NULL,
    weekly_average_volume   NUMERIC(20, 4) NOT NULL,
    ingested_at             TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

-- Remove the obsolete link after legacy rows have been migrated above.
